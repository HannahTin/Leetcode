class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right
class Solution:
    def diameterOfBinaryTree(self, root: TreeNode) -> int:
        res = 0
        def dfs(node):
            nonlocal res
            if (node == None): return 0
            left = dfs(node.left)
            right = dfs(node.right)
            res = max(res,left+right)
            return max(left,right)+1
        dfs(root)
        return res